package com.dev.vo;

public class MemberVO {
	private String id;
	private String passwd;
	private String name;
	private String mail;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}

	public String getPasswd() {
		return passwd;
	}

	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String nail) {
		this.mail = nail;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	

}
